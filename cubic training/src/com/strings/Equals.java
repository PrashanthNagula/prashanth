package com.strings;

public class Equals {
public static void main(String[] args) {
	String s1="prashanth";
	String s2="prashanth";
	String s3="PRASHANTH";
	String s4="manoj";
	System.out.println(s1.equalsIgnoreCase(s2));
	System.out.println(s1.equalsIgnoreCase(s3));
	System.out.println(s1.equalsIgnoreCase(s4));
}
}
